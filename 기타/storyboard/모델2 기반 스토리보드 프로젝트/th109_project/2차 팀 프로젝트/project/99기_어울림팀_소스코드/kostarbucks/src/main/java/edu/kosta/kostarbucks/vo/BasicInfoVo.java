package edu.kosta.kostarbucks.vo;

public class BasicInfoVo {
	private String chain_id, address, chain_phone, license_num, bank, bank_num, img, open_date;
	private int capital;
	
	public BasicInfoVo() {	}
	
	public String getChain_id() {
		return chain_id;
	}
	public void setChain_id(String chain_id) {
		this.chain_id = chain_id;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getChain_phone() {
		return chain_phone;
	}
	public void setChain_phone(String chain_phone) {
		this.chain_phone = chain_phone;
	}
	public String getLicense_num() {
		return license_num;
	}
	public void setLicense_num(String license_num) {
		this.license_num = license_num;
	}
	public String getBank() {
		return bank;
	}
	public void setBank(String bank) {
		this.bank = bank;
	}
	public String getBank_num() {
		return bank_num;
	}
	public void setBank_num(String bank_num) {
		this.bank_num = bank_num;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public int getCapital() {
		return capital;
	}
	public void setCapital(int capital) {
		this.capital = capital;
	}

	public String getOpen_date() {
		return open_date;
	}

	public void setOpen_date(String open_date) {
		this.open_date = open_date;
	}
}
