package edu.kosta.kostarbucks.vo;

import java.util.Date;

public class IdeaBoardVo {
	private String idea_title, mem_name, idea_state, idea_content;
	private int idea_num,idea_readcount;
	private Date idea_date;
	
	public IdeaBoardVo() {
	}
	
	public String getIdea_title() {
		return idea_title;
	}
	public void setIdea_title(String idea_title) {
		this.idea_title = idea_title;
	}
	public String getMem_name() {
		return mem_name;
	}
	public void setMem_name(String mem_name) {
		this.mem_name = mem_name;
	}
	
	public Date getIdea_date() {
		return idea_date;
	}

	public void setIdea_date(Date idea_date) {
		this.idea_date = idea_date;
	}

	public String getIdea_state() {
		return idea_state;
	}
	public void setIdea_state(String idea_state) {
		this.idea_state = idea_state;
	}
	public String getIdea_content() {
		return idea_content;
	}
	public void setIdea_content(String idea_content) {
		this.idea_content = idea_content;
	}
	public int getIdea_num() {
		return idea_num;
	}
	public void setIdea_num(int idea_num) {
		this.idea_num = idea_num;
	}
	public int getIdea_readcount() {
		return idea_readcount;
	}
	public void setIdea_readcount(int idea_readcount) {
		this.idea_readcount = idea_readcount;
	}
	
	
}
