package edu.kosta.kostarbucks.vo;

public class ChainListVo {
	private String chain_id, chain_pwd, chain_name;

	public ChainListVo() {}
	
	

	public String getChain_id() {
		return chain_id;
	}

	public void setChain_id(String chain_id) {
		this.chain_id = chain_id;
	}

	public String getChain_pwd() {
		return chain_pwd;
	}

	public void setChain_pwd(String chain_pwd) {
		this.chain_pwd = chain_pwd;
	}

	public String getChain_name() {
		return chain_name;
	}

	public void setChain_name(String chain_name) {
		this.chain_name = chain_name;
	}
}
